#include "includes/libft.h"

int		ft_isalnum(int c)
{
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
		return (sizeof(c) * 2);
	if (c >= '0' && c <= '9')
		return (sizeof(c) * 2);
	else
		return (0);
}
